    public void Ordenar(String orden){
    String [] vec;
    vec=abrirarchivo.getTexto().split(",");
    int n=0;
    switch(orden){
        case "p":
            System.out.println("Orden Pila");
        Pila pila = new Pila(0);
        for(int i=0;i<vec.length;i++){
            n=Integer.parseInt(vec[i]);
           
               pila.Apilar(n);
        }
        pila.Imprimir();
            System.out.println("");
       break;
        case"c":
             System.out.println("Orden Cola");
         Cola cola = new Cola(0);
           for(int i=0;i<vec.length;i++){
            n=Integer.parseInt(vec[i]);
          
               cola.Encolar(n);
            }
          cola.Imprimir();
            System.out.println("");
        break;
        case"lc":
             System.out.println("Lista Circular");
           ListaCircular listacircular = new ListaCircular(0);  
           for(int i=0;i<vec.length;i++){
            n=Integer.parseInt(vec[i]);
               
               listacircular.InsertarAlFinal(n);
            }
        
           listacircular.Imprimir();
            System.out.println("");
        break;
        case"lc2":
             System.out.println("Lista Doble");
           ListaDoble listadoble = new ListaDoble(0);  
           for(int i=0;i<vec.length;i++){
            n=Integer.parseInt(vec[i]);
         
               listadoble.InsertarAlFrente(n);
            }
           listadoble.Imprimir();
            System.out.println("");
        break;
    
    }
    
    
    
    }
   
    public String AbrirArchivo(){
     String nombrearchivo="";
        //Crean los filtros para el tipo de archivo que van a permitir que se abran, en este caso es solo es bbm
        FileNameExtensionFilter filtro = new FileNameExtensionFilter(".txt","txt");
        
        //inicializan el dialogo para escoger textos
        abrirarc=new JFileChooser();  
        
        //Le agregan el filtro al dialogo de texto
        abrirarc.setFileFilter(filtro);
        
        //Es la variable entero que guarda el tipo de operacion que se hace, aceptar o cancelar
        int returnVal = abrirarc.showOpenDialog(this);
        
        //Verifica el tipo de accion que se realizÃ³, si se dio click en "Aceptar" obtiene la ruta del doc seleccionado
        if(returnVal == JFileChooser.APPROVE_OPTION) {
            nombrearchivo=abrirarc.getSelectedFile().getPath();
         //Guarda en una variable global el nombre del archivo que se encuentra abierto,
         //si se hacen cambios y le damos click en guardar, sobreescribe el archivo
          archivo_Abierto = nombrearchivo;
        }
    
    return nombrearchivo;
    }
